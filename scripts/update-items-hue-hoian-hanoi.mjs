// ─── Reuse bracket-counting replace util from part 1 ────────────────────────
function replaceItemsBlock(content, nodeId, newItemsBlock) {
  const nodePattern = new RegExp(`(id:\\s*"${nodeId}"[\\s\\S]*?items:\\s*)\\[`, "m");
  const match = content.match(nodePattern);
  if (!match) {
    console.warn(`  ⚠️  Node "${nodeId}" not found`);
    return content;
  }
  const matchStart = match.index + match[0].length - 1;
  let depth = 0;
  let i = matchStart;
  while (i < content.length) {
    if (content[i] === "[") depth++;
    else if (content[i] === "]") {
      depth--;
      if (depth === 0) break;
    }
    i++;
  }
  const matchEnd = i + 1;
  return content.slice(0, matchStart) + newItemsBlock + content.slice(matchEnd);
}

